#ifndef _LCOM_I8042_H_
#define _LCOM_I8042_H_

/** @defgroup i8042 i8042
 * @{
 *
 * Constants for programming the i8042 Keyboard.
 */



#define BIT(n) 				(0x01<<(n))

#define TIMER0_IRQ	        0

#define DELAY_US 			20000


/* I/O port addresses */

#define OUT_BUF				0x64

#define IN_BUF				0x60


/* Commands */

#define LED_CMD				0xED

#define ESC_COD				0x81

#define CMD_ACK				0xFA

#define CMD_RES				0xFE

#define CMD_ERR				0xFC

#define SPECIAL				0xE0

/**@}*/

#endif /* _LCOM_I8042_H */
